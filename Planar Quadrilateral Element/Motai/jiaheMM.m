function [g]=jiaheMM(MM,m,mm)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%该函数进行单元一致质量矩阵的组装
%%%%输入:单元一致质量矩阵m，单元的节点编号i,j,k(ME(i,:)),
%%%%单位节点维数n
%%%%输出:整体质量矩阵KK
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
i=mm(1,1);  %单元左侧节点的序号
j=mm(1,2);  %单元右侧节点的序号
    Dof(1)=i*3-2;
    Dof(2)=i*3-1;
    Dof(3)=i*3;
    Dof(4)=j*3-2;
    Dof(5)=j*3-1;
    Dof(6)=j*3;
for n1=1:6
    for n2=1:6
        MM(Dof(n1),Dof(n2))=MM(Dof(n1),Dof(n2))+m(n1,n2);
    end
end
g=MM;